    private void seachem() {
          try {
     ResultSet rs = db.serch("select * from po_item where po_id='"+txtpoid.getText()+"'");
         DefaultTableModel dtm =(DefaultTableModel)jTable1.getModel();
         dtm.setRowCount(0);
         while(rs.next()){
          Vector v =new Vector ();
         v.add(rs.getString("item_id"));
           ResultSet serch = db.serch("SELECT * FROM item WHERE item_id='"+rs.getString("item_id")+"'");
         if(serch.next()){
         v.add(serch.getString("name"));
         }
         v.add(rs.getString("qty"));
          
         
            dtm.addRow(v);
            
         }
        // txtSearch.setText(null);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadText2() {
        try {
            ResultSet serch = db.serch("SELECT * FROM po WHERE po_id='"+txtpoid.getText().toUpperCase()+"'");
            if(serch.next()){
           txtsup.setText(serch.getString("suplayer"));
           jDateChooser3.setDate(serch.getDate("receve_date"));
           pode.setText(serch.getString("description"));
          
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
