    private void txtpoidKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtpoidKeyReleased
        try{
            if(!txtpoid.getText().trim().equals("")){
                if(txtpoid.getText().toUpperCase().contains("P")){
                    ResultSet rs=  db.serch("SELECT * FROM po WHERE stat=1 AND po_id  LIKE '"+txtpoid.getText().toUpperCase()+"%'");
                    java.util.Vector v = new java.util.Vector();
                    jScrollPane4.setVisible(false);
                    while(rs.next()){
                        jScrollPane4.setVisible(true);
                        v.add(rs.getString("po_id"));
                    }
                    list2.setListData(v);
                }
            }else{
                jScrollPane4.setVisible(false);
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_txtpoidKeyReleased

