    private void updateDetails() {
        if(jRadioButton1.isSelected()){
             try {
         
          
          
        db.iud("update user_account set stat='"+"1"+"' where employee_id='"+txtSearch.getText()+"'");
          JOptionPane.showMessageDialog(this, " "+ txtSearch.getText()+" user is Active");
          txtSearch.setText(null);
          jRadioButton1.setSelected(true);

        } catch (Exception e) {
            e.printStackTrace();
        }
            
        }else if(jRadioButton2.isSelected()){
            try {
            db.iud("update user_account set stat='"+"0"+"' where employee_id='"+txtSearch.getText()+"'");
          JOptionPane.showMessageDialog(this, " "+ txtSearch.getText()+" user is Remove");
           txtSearch.setText(null);
          jRadioButton1.setSelected(true);


        } catch (Exception e) {
            e.printStackTrace();
        }
        }
    }

