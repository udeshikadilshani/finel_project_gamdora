    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        try{
            if(!txtSearch.getText().trim().equals("")){
            if(txtSearch.getText().toUpperCase().contains("E")){
                ResultSet rs=  db.serch("SELECT * FROM user_account WHERE stat=1 AND employee_id  LIKE '"+txtSearch.getText().toUpperCase()+"%'");
                Vector v = new Vector();
                       jScrollPane1.setVisible(false);
                while(rs.next()){
                     jScrollPane1.setVisible(true);
                    v.add(rs.getString("employee_id")+"-"+rs.getString("user_name"));
                }
                jList1.setListData(v);
            }else{
                ResultSet rs=  db.serch("SELECT * FROM user_account WHERE stat=1 AND user_name LIKE '"+txtSearch.getText().toUpperCase()+"%'");
                Vector v = new Vector();
                 jScrollPane1.setVisible(false);
                while(rs.next()){
                    jScrollPane1.setVisible(true);
                    v.add(rs.getString("employee_id")+"-"+rs.getString("user_name"));  
                }
                jList1.setListData(v);
            }
            }else{
             jScrollPane1.setVisible(false);
            }
}catch(Exception e){
e.printStackTrace();
}
    }//GEN-LAST:event_txtSearchKeyReleased

