    private void genareteMemberID() {
         try {
            ResultSet rs = db.serch("select count(employee_id) as x from employee");
            if(rs.next()){
                String counts = rs.getString("x");
                
                int count = Integer.parseInt(counts);
               ++count;
                if(count<10){
                txtemid.setText("E000"+count);
               // txtmi.setEditable(false);
                }else if(count<100){
                 txtemid.setText("E00"+count);
               // txtmi.setEditable(false);
                }else if(count<1000){
                    txtemid.setText("E0"+count);
                //txtmi.setEditable(false);
                }else if(count<10000){
                    txtemid.setText("E"+count);
               // txtmi.setEditable(false);
                }
                  txtnic.grabFocus();
            }
           
        } catch (Exception e) {
            System.out.println(e);
        }
    }

