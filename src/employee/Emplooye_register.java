             String nic= txtnic.getText().toUpperCase();
            String firstName= txtfname.getText().toUpperCase();
            String lastName= txtlname.getText().toUpperCase();
           Date dob= caledob.getDate();
           SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
           String adL1= txtad1.getText().toUpperCase();
           String adL2= txtad2.getText().toUpperCase();
           String adL3= txtad3.getText().toUpperCase();
            String contact =txtcno.getText().toUpperCase();
           String registerDate = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(new Date());
            db.iud("insert into employee values('"+employeeId+"','"+nic+"','"+firstName+"','"+lastName+"','"+sdf.format(dob)+"','"+adL1+"','"+adL2+"','"+adL3+"','"+contact+"','"+registerDate+"','"+1+"')");
           
