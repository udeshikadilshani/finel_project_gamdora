    private void addno() {
         try {
            ResultSet rs = db.serch("select count(item_id) as x from item");
            if(rs.next()){
                String counts = rs.getString("x");
                
                int count = Integer.parseInt(counts);
               ++count;
                if(count<10){
                txtitno.setText("IT000"+count);
               // txtmi.setEditable(false);
                }else if(count<100){
                 txtitno.setText("IT00"+count);
               // txtmi.setEditable(false);
                }else if(count<1000){
                    txtitno.setText("IT0"+count);
                //txtmi.setEditable(false);
                }else if(count<10000){
                    txtitno.setText("IT"+count);
               // txtmi.setEditable(false);
                }
                  txtitna.grabFocus();
            }
           
        } catch (Exception e) {
            System.out.println(e);
        }
    }

