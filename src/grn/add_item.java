    private void GRNNO() {
          try {
            ResultSet rs = db.serch("select count(grn_id) as x from grn");
            if(rs.next()){
                String counts = rs.getString("x");
                
                int count = Integer.parseInt(counts);
               ++count;
                if(count<10){
                txtgrn.setText("GR000"+count);
               // txtmi.setEditable(false);
                }else if(count<100){
                 txtgrn.setText("GR00"+count);
               // txtmi.setEditable(false);
                }else if(count<1000){
                    txtgrn.setText("GR0"+count);
                //txtmi.setEditable(false);
                }else if(count<10000){
                    txtgrn.setText("GR"+count);
               // txtmi.setEditable(false);
                }
                 
            }
           
        } catch (Exception e) {
            System.out.println(e);
        }
    }

    private void tbData() {
       try {
            DefaultTableModel dtm =(DefaultTableModel)jTable1.getModel();
          
            
         Vector v =new Vector ();
         v.add(txtItemId.getText());
           ResultSet serch = db.serch("SELECT * FROM item WHERE item_id='"+txtItemId.getText()+"'");
         if(serch.next()){
         v.add(serch.getString("name"));
         }
         v.add(txtQty.getText());
         DecimalFormat df = new DecimalFormat("0.00");
           String buingPrice =df.format(Double.parseDouble(txtBuyPrice.getText()));
           String sellingPrice =df.format(Double.parseDouble(txtSellPrice.getText()));
           String discount1 =df.format(Double.parseDouble(txtDiscount.getText()));
         v.add(buingPrice);
           System.out.println(buingPrice);
         v.add(sellingPrice);
         v.add(discount1);
            dtm.addRow(v);
            
            double amount= Double.parseDouble(txtAmount.getText());
            amount=amount+(Double.parseDouble(txtBuyPrice.getText()))*Integer.parseInt(txtQty.getText());
            txtAmount.setText(String.valueOf(amount));
            
            double discount= Double.parseDouble(txtTotDis.getText());
            discount=discount+(Double.parseDouble(txtDiscount.getText()))*Integer.parseInt(txtQty.getText());
            txtTotDis.setText(String.valueOf(discount));
            txtNetAm.setText(String.valueOf(amount-discount));
            clearTextFeald();
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

