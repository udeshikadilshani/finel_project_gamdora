    private void txtSupKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSupKeyReleased
        try{
            if(!txtSup.getText().trim().equals("")){
                if(txtSup.getText().toUpperCase().contains("S")){
                    ResultSet rs=  db.serch("SELECT * FROM  suplayer WHERE stat=1 AND suplayer_id  LIKE '"+txtSup.getText().toUpperCase()+"%'");
                    java.util.Vector v = new java.util.Vector();
                    jscole1.setVisible(false);
                    while(rs.next()){
                        jscole1.setVisible(true);
                        v.add(rs.getString("suplayer_id")+"-"+rs.getString("name"));
                    }
                    list3.setListData(v);
                }else{
                    ResultSet rs=  db.serch("SELECT * FROM suplayer WHERE stat=1 AND name LIKE '"+txtSup.getText().toUpperCase()+"%'");
                    java.util.Vector v = new java.util.Vector();
                    jscole1.setVisible(false);
                    while(rs.next()){
                        jscole1.setVisible(true);
                        v.add(rs.getString("suplayer_id")+"-"+rs.getString("name"));
                    }
                    list3.setListData(v);
                }
            }else{
                jscole1.setVisible(false);
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_txtSupKeyReleased

